//Ολα οριζονται με κλασεις στην java
//Η κλαση πρεπει να εχει το ιδιο ονομα με το αρχειο
//Η κλαση πρεπει να ειναι public
//Ο κωδικας πρεπει να ειναι μεσα σε μεθοδου
public class Hello {
    //Η μεθοδος που θα τρεξει πρωτη ειναι η main
    //Η main ειναι μεθοδος της κλασης
    //Η main ειναι public, static, void
    //Η main δεν παιρνει ορισματα
    //Η main ειναι παντα η πρωτη μεθοδος που τρεχει
    public static void main(String[] args) {
        System.out.println("Hello World");
    }
}

